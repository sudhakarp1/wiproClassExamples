'''
    Numbers in Python

'''

var  = 10 
print(f'In decimal var: {var}')
print(f'In octal {var:o}')
print(f'var in Hexa: {var:x}')
print(f'In Binary {var:b}')

print(f'In decimal var: {~var}')
print(f'In octal {~var:o}')
print(f'var in Hexa: {~var:x}')
print(f'In Binary {~var:b}')

